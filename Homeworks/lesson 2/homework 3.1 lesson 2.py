"""
Создайте ряд функций для проведения математических вычислений:
● функция вычисления факториала числа (произведение натуральных чисел от 1
до n). Принимает в качестве аргумента число, возвращает его факториал;
● поиск наибольшего числа из трёх. Принимает в качестве аргумента кортеж из
трёх чисел, возвращает наибольшее из них;
● расчёт площади прямоугольного треугольника. Принимает в качестве аргумента
размер двух катетов треугольника. Возвращает площадь треугольника.
"""

def factorial(n):

    if n < 0:
        raise ValueError("Факториал определен только для неотрицательных чисел.")
    if n == 0 or n == 1:
        return 1
    result = 1
    for i in range(2, n + 1):
        result *= i
    return result


def max_of_three(numbers):

    if len(numbers) != 3:
        raise ValueError("Функция принимает кортеж из трех чисел.")
    return max(numbers)


def triangle_area(a, b):

    if a <= 0 or b <= 0:
        raise ValueError("Длины катетов должны быть положительными числами.")
    return 0.5 * a * b

if __name__ == "__main__":
    # Факториал числа
    num = int(input("Введите число для вычисления его факториала: "))
    print(f"Факториал {num} равен {factorial(num)}")

    # Наибольшее из трех чисел
    nums = tuple(map(int, input("Введите три числа, разделенные пробелами: ").split()))
    print(f"Наибольшее число из {nums} равно {max_of_three(nums)}")

    # Площадь прямоугольного треугольника
    a = float(input("Введите длину первого катета: "))
    b = float(input("Введите длину второго катета: "))
    print(f"Площадь треугольника с катетами {a} и {b} равна {triangle_area(a, b)}")